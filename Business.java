package my.food.business_marketing;

public class Business {

    String BusinessName;
    String BusinessDescription;
    String BusinessTime;
    String BusinessContact;
    String BusinessWebsite;
    String BusinessLattitude;
    String BusinessLongitude;

    public String getBusinessName() {
        return BusinessName;
    }

    public void setBusinessName(String businessName) {
        BusinessName = businessName;
    }

    public String getBusinessDescription() {
        return BusinessDescription;
    }

    public void setBusinessDescription(String businessDescription) {
        BusinessDescription = businessDescription;
    }

    public String getBusinessTime() {
        return BusinessTime;
    }

    public void setBusinessTime(String businessTime) {
        BusinessTime = businessTime;
    }

    public String getBusinessContact() {
        return BusinessContact;
    }

    public void setBusinessContact(String businessContact) {
        BusinessContact = businessContact;
    }

    public String getBusinessWebsite() {
        return BusinessWebsite;
    }

    public void setBusinessWebsite(String businessWebsite) {
        BusinessWebsite = businessWebsite;
    }

    public String getBusinessLattitude() {
        return BusinessLattitude;
    }

    public void setBusinessLattitude(String businessLattitude) {
        BusinessLattitude = businessLattitude;
    }

    public String getBusinessLongitude() {
        return BusinessLongitude;
    }

    public void setBusinessLongitude(String businessLongitude) {
        BusinessLongitude = businessLongitude;
    }
}
